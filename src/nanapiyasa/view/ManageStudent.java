    private void editBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editBtnMouseClicked
        int res = JOptionPane.showOptionDialog(this, "Dou you want to save changes?", "Options", 0, 0, null, null, null);
        if (res == 0) {
            String id = (String) stIdCombo.getSelectedItem();
            String name = nameTxt.getText();
            String address = addressTxt.getText();
            String tel = telTxt.getText();
            String dob = dobTxt.getText();
            String gender = genderTxt.getText();
            String guard = guardTxt.getText();
            String occupation = occupationTxt.getText();
            String relationship = relationshipTxt.getText();
            String guardTel = guardTelTxt.getText();

            Student student = new Student(id, name, address, tel, dob, gender, guard, occupation, relationship, guardTel);

            try {
                boolean isUpdated = StudentController.updateStudent(student);
                if (isUpdated) {
                    JOptionPane.showMessageDialog(this, "Successfully Saved");
                } else {
                    JOptionPane.showMessageDialog(this, "Saving Failed");
                }
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(ManageStudent.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(ManageStudent.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_editBtnMouseClicked

    private void deleteBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deleteBtnMouseClicked
        int res = JOptionPane.showOptionDialog(this, "Dou you want to Delete this Student?", "Options", 0, 0, null, null, null);
        if (res == 0) {
            String id = (String) stIdCombo.getSelectedItem();
            try {

                boolean isDeleted = StudentController.deleteStudent(id);
                if (isDeleted) {
                    JOptionPane.showMessageDialog(this, "Student Removed");
                    stIdCombo.setSelectedItem("none");
                    arrangeCombo();
                } else {
                    JOptionPane.showMessageDialog(this, "Try Again");
                }
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(SignUp.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(SignUp.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_deleteBtnMouseClicked


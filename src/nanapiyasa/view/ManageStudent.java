    private void stIdComboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_stIdComboActionPerformed

        String id = (String) stIdCombo.getSelectedItem();
        if (id != "none") {
            try {
                Student student = StudentController.searchStudent(id);
                if (student != null) {
                    nameTxt.setText(student.getStName());
                    addressTxt.setText(student.getStAddress());
                    telTxt.setText(student.getStTelephone());
                    dobTxt.setText(student.getdOB());
                    genderTxt.setText(student.getGender());
                    guardTxt.setText(student.getGuardName());
                    occupationTxt.setText(student.getGuardOccupation());
                    relationshipTxt.setText(student.getGuardRelationship());
                    guardTelTxt.setText(student.getGuardTelephone());
                } else {
                    JOptionPane.showMessageDialog(this, "Student Does Not Exist");
                    stIdCombo.setSelectedItem("none");
                }
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(ManageStudent.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(ManageStudent.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            nameTxt.setText(null);
            addressTxt.setText(null);
            telTxt.setText(null);
            dobTxt.setText(null);
            genderTxt.setText(null);
            guardTxt.setText(null);
            occupationTxt.setText(null);
            relationshipTxt.setText(null);
            guardTelTxt.setText(null);
        }
    }//GEN-LAST:event_stIdComboActionPerformed

